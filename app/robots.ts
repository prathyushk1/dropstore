// app/robots.ts
import type { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: ['/admin/', '/api/', '/test-db/'],
      },
    ],
    sitemap: 'https://dropstor.online/sitemap.xml',
  }
}
